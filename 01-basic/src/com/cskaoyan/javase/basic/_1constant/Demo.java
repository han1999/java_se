/**
 * 该Demo类探讨什么是常量
 *
 * 常量指的是不变的变量
 * 常量分为两类:
 *      1,字面值常量
 *      像双引号引起来的字符串 或者 给变量赋值的1,2,3,3.3等等都属于字面值常量
 *      字面值常量都是直接用数值表示的,它不能单独使用
 *
 *      2,自定义常量
 *      自定义常量是通过语法将变量变为常量
 *
 */
public class Demo {
    public static void main(String[] args) {
        System.out.println("hello world!");
    }
}
