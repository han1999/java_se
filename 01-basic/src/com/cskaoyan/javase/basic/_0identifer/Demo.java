/**
 * 该Demo类主要探讨Java当中标识符的命名规范
 *
 * 1,包名
 *  在工作当中,我们习惯反转公司域名,作为开发包名的规范
 *  baidu.com --> com.baidu
 *  bilibili.com --> com.bilibili
 *  cskaoyan.com --> com.cskaoyan
 *  注意事项:
 *      包名普遍都是小写的,几乎很少有人用大写
 *      包名当中很少有字符或者数字
 *
 * 2,类名
 *  类名一定要遵循大驼峰式的命名规范
 *  也就是说像demo,test这种都是错误的命名
 *  比如Student/MyJava
 *  其次,给一个类起名字不要瞎起,见到这个类名,应该大体上知道这个类干啥
 *  起名字都应该使用英文单词,不能使用汉语,更不能用拼音
 *
 * 3,变量名
 *  变量名应该遵循小驼峰式的命名规范
 *  Demo/Test都是错误的
 *  其次变量名也应该尽量用英文单词,除非万不得已不要用拼音这种东西
 *
 *
 *
 */
public class Demo {
    public static void main(String[] args) {
        //int student;
        //int studnet; 单词拼写错误
        //int l1; //在使用l的时候不要和1混用,如果要用,建议用大写的L
    }
}
